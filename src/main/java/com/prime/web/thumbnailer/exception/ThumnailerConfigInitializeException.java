package com.prime.web.thumbnailer.exception;

public class ThumnailerConfigInitializeException extends RuntimeException
{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ThumnailerConfigInitializeException() {
		super();
	}

	public ThumnailerConfigInitializeException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ThumnailerConfigInitializeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ThumnailerConfigInitializeException(String message) {
		super(message);
	}

	public ThumnailerConfigInitializeException(Throwable cause) {
		super(cause);
	}
	
	
	
}
